import java.util.Scanner;

public class Ladder {
	public static void main(String...args) {
		Scanner sc=new Scanner(System.in);
		int n=sc.nextInt();
		for(int i=0;i<n;i++) {
			//System.out.println();
			System.out.println("*   *\n" + 
					"*   *\n" + 
					"*****");
		}
		System.out.println("*   *\n" +"*   *\n"); 
				
	}

}
